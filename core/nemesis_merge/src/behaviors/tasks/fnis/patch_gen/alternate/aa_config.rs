// Public entry point
// =============================================================================

/// Collects `AAMod` data from a slice of `AnimIoJob` and generates `aa_config.json`.
///
/// Call this once after all `alt_anim_to_oar` jobs have been collected.
/// Load order is preserved via `IndexMap` to ensure a stable CRC.
///
/// # Output path
/// `SKSE/Plugins/fnis_aa/config.json`
pub fn build_aa_config_from_jobs(jobs: &[AnimIoJob], output_dir: &Path) -> Result<BaseMap, Error> {
    // IndexMap preserves insertion order = mod load order, giving a stable CRC.
    let mut aa_mods = IndexMap::new();

    for job in jobs {
        let AnimIoJob::Hkx(ConversionJob {
            kind:
                AnimKind::FnisAA {
                    prefix,
                    group_name,
                    slot_count,
                    ..
                },
            ..
        }) = job
        else {
            continue;
        };

        let aa_mod = aa_mods.entry(Arc::clone(prefix)).or_insert_with(|| AAMod {
            prefix: Arc::clone(prefix),
            name: Arc::clone(prefix),
            mod_id: 0, // assigned later by compute_bases()
            groups: vec![],
        });

        // Each group appears once per mod regardless of how many animation
        // files reference it.
        if !aa_mod.groups.iter().any(|g| g.name == *group_name) {
            aa_mod.groups.push(AAGroup {
                name: *group_name,
                base: 0, // assigned later by compute_bases()
                slot_count: *slot_count,
            });
        }
    }

    write_aa_config(aa_mods.into_values().collect(), output_dir)
}

