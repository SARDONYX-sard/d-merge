//! Generates the SKSE plugin configuration for FNIS alternate animation variables.
//!
//! Computes per-mod base slot values from load order and produces
//! `aa_config.json` consumed by the fnis_aa SKSE plugin at runtime.
use std::{borrow::Cow, path::Path, sync::Arc};

use indexmap::IndexMap;
use serde::Serialize;

use super::group_names::AAGroupName;
use crate::{
    behaviors::tasks::fnis::patch_gen::io_jobs::{AnimIoJob, AnimKind, ConversionJob},
    errors::Error,
};

/// `(prefix, group_id)` → `base` lookup built from a finalized `AAConfig`.
pub type BaseMap = std::collections::HashMap<(Arc<str>, u64), u64>;

/// Builds the base lookup map from a finalized [`AAConfig`].
///
/// Key: `(prefix, group_id)` — matches the fields in [`FnisAAConfigJob`].
fn build_base_map(config: &AAConfig) -> BaseMap {
    config
        .mods
        .iter()
        .flat_map(|m| {
            m.groups
                .iter()
                .map(|g| ((Arc::clone(&m.prefix), g.name.group_id()), g.base))
        })
        .collect()
}

// =============================================================================
// Data model
// =============================================================================

/// JSON schema URI embedded in every generated `config.json`.
const SCHEMA_URI: &str =
    "https://raw.githubusercontent.com/SARDONYX-sard/d-merge/refs/heads/main/tools/schemas/aa_config.schema.json";

#[derive(Debug, Clone, PartialEq, Hash, Serialize)]
#[cfg_attr(feature = "schema", derive(schemars::JsonSchema))]
pub struct AAGroup {
    /// FNIS group identifier.
    ///
    /// Serializes to the FNIS wire string (e.g. `"_1hmeqp"`).
    /// The corresponding `group_id` integer is derived via [`AAGroupName::group_id`].
    pub name: AAGroupName,

    /// First slot index occupied by this mod for this group.
    ///
    /// base 0 is always reserved for vanilla. Custom slots start at 1 and are
    /// assigned contiguously in mod load order:
    /// ```text
    /// slot 0..N     mod A  (base=1, slot_count=N)
    /// slot N+1..M   mod B  (base=N+1, slot_count=M-N)
    /// ```
    pub base: u64,

    #[serde(skip)]
    /// Number of animation styles registered by this mod for this group.
    ///
    /// Determines how many slots this mod occupies starting from `base`.
    ///
    /// It is used to calculate the base of mods with a lower priority than this one.
    pub slot_count: u64,
}

#[derive(Debug, Clone, PartialEq, Serialize)]
#[cfg_attr(feature = "schema", derive(schemars::JsonSchema))]
pub struct AAMod {
    /// 3-character mod prefix used by FNIS, e.g. `xpe` for XPMSE.
    ///
    /// Used as the key in `FNIS_aa2::GetAAprefixList()`.
    pub prefix: Arc<str>,

    /// Full mod name, e.g. `XPMSE`.
    ///
    /// Used for logging and debug output only.
    pub name: Arc<str>,

    /// Index of this mod in load order, assigned by d_merge.
    ///
    /// Corresponds to the `PP` component of the `PPGGBB` encoded set string.
    pub mod_id: u64,

    /// Animation groups registered by this mod, sorted by `group_id`.
    pub groups: Vec<AAGroup>,
}

/// Root configuration for the `fnis_aa` SKSE plugin.
///
/// Generated by [d_merge](https://github.com/SARDONYX-sard/d_merge) and read
/// at game startup by `fnis_aa.dll`. Replaces the slot layout that the FNIS
/// generator previously baked into `FNIS_aa2.pex`.
///
/// # File location
/// `SKSE/Plugins/fnis_aa/config.json`
///
/// # Example
/// ```json
/// {
///   "$schema": "https://raw.githubusercontent.com/SARDONYX-sard/fnis_aa/main/schema/aa_config.schema.json",
///   "crc": 123456,
///   "fnis_version": "V07.06.00.0",
///   "mods": [
///     {
///       "prefix": "xpe",
///       "name": "XPMSE",
///       "mod_id": 0,
///       "groups": [
///         { "name": "_1hmeqp", "base": 1, "slot_count": 5 }
///       ]
///     }
///   ]
/// }
/// ```
#[derive(Debug, Serialize)]
#[cfg_attr(feature = "schema", derive(schemars::JsonSchema))]
pub struct AAConfig {
    /// URI of the JSON schema for this file.
    ///
    /// Always set to the canonical schema URI. Enables schema validation in
    /// editors such as VS Code.
    #[serde(rename = "$schema")]
    pub schema: Cow<'static, str>,

    /// CRC fingerprint of the current slot layout.
    ///
    /// Computed from mod load order and slot counts. Written to
    /// `FNISaa_crc` on the actor at game load. If the saved value differs,
    /// all animation variables must be re-applied to reflect the new layout.
    pub crc: u32,

    /// FNIS version string returned by `FNISVersionGenerated.get()`.
    ///
    /// e.g. `V07.06.00.0`
    pub fnis_version: Cow<'static, str>,

    /// FNIS version string returned by `fnis.pex`.
    pub fnis_creature_version: Cow<'static, str>,

    /// All mods registered in load order.
    ///
    /// Load order determines `mod_id` assignment and therefore `base` values.
    /// Changing load order produces a different `crc`.
    pub mods: Vec<AAMod>,
}

impl AAConfig {
    /// Creates a new `AAConfig`.
    ///
    /// When the `schema` feature is enabled, the canonical schema URI is
    /// embedded in the output JSON as `$schema`.
    pub fn new(fnis_version: impl Into<Cow<'static, str>>, mods: Vec<AAMod>) -> Self {
        let fnis_version = fnis_version.into();
        Self {
            schema: Cow::Borrowed(SCHEMA_URI),
            crc: compute_crc(&mods),
            fnis_version: fnis_version.clone(),
            fnis_creature_version: fnis_version,
            mods,
        }
    }
}

// =============================================================================
// Base computation
// =============================================================================

/// Assigns `base` and `mod_id` for every group across all mods in load order.
/// Slot 0 is always reserved for vanilla; custom slots start at 1.
fn compute_bases(mods: &mut [AAMod]) {
    let mut next_slot = std::collections::HashMap::new();

    for (mod_id, aa_mod) in mods.iter_mut().enumerate() {
        aa_mod.mod_id = mod_id as u64;
        for group in aa_mod.groups.iter_mut() {
            let slot = next_slot.entry(group.name.group_id()).or_insert(1);
            group.base = *slot;
            *slot += group.slot_count;
        }
    }
}

/// CRC fingerprint of the current slot layout.
/// Any change in load order or slot counts invalidates saved Papyrus values.
fn compute_crc(mods: &[AAMod]) -> u32 {
    use std::hash::{Hash, Hasher};
    let mut hasher = std::collections::hash_map::DefaultHasher::new();
    for aa_mod in mods {
        for group in &aa_mod.groups {
            group.hash(&mut hasher);
        }
    }
    hasher.finish() as u32
}

// =============================================================================
// Output
// =============================================================================

fn write_aa_config(mods: Vec<AAMod>, output_dir: &Path) -> Result<BaseMap, Error> {
    let mut mods = mods;
    compute_bases(&mut mods);
    let config = AAConfig::new("V07.06.00.0", mods);

    let path = output_dir.join("SKSE/Plugins/fnis_aa/config.json");
    let json = sonic_rs::to_string_pretty(&config).map_err(|e| Error::JsonError {
        path: path.clone(),
        source: e,
    })?;
    if let Some(parent) = path.parent() {
        std::fs::create_dir_all(parent).map_err(|e| Error::FailedIo {
            path: parent.to_path_buf(),
            source: e,
        })?;
    }
    std::fs::write(&path, &json).map_err(|e| Error::FailedIo {
        path: path.clone(),
        source: e,
    })?;

    Ok(build_base_map(&config))
}

